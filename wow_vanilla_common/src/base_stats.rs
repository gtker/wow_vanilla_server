use wow_world_base::vanilla::{Class, Race};

pub const fn health_bonus_from_stamina(stamina: u8) -> u16 {
    let base_stamina = if stamina < 20 { stamina as u16 } else { 20 };
    let additional_stamina = stamina as u16 - base_stamina;

    base_stamina + (additional_stamina * 10)
}

pub const fn calculate_health(base_health: u16, stamina: u8) -> u16 {
    base_health + health_bonus_from_stamina(stamina)
}

pub const fn mana_bonus_from_intellect(intellect: u8) -> u16 {
    let base_intellect = if intellect < 20 { intellect as u16 } else { 20 };
    let additional_intellect = intellect as u16 - base_intellect;

    base_intellect + (additional_intellect * 15)
}

pub const fn calculate_mana(base_mana: u16, intellect: u8) -> u16 {
    base_mana + mana_bonus_from_intellect(intellect)
}

pub const fn get_base_stats(race: Race, class: Class, level: u8) -> BaseStats {
    let level = if level >= 60 {
        59 as usize
    } else if level != 0 {
        level as usize - 1
    } else {
        0
    };

    let table = match race {
        Race::Human => match class {
            Class::Paladin => HUMAN_PALADIN,
            Class::Rogue => HUMAN_ROGUE,
            Class::Priest => HUMAN_PRIEST,
            Class::Mage => HUMAN_MAGE,
            Class::Warlock => HUMAN_WARLOCK,
            Class::Warrior | Class::Hunter | Class::Shaman | Class::Druid => HUMAN_WARRIOR,
        },
        Race::Orc => match class {
            Class::Paladin | Class::Priest | Class::Mage | Class::Druid | Class::Warrior => {
                ORC_WARRIOR
            }
            Class::Hunter => ORC_HUNTER,
            Class::Rogue => ORC_ROGUE,
            Class::Shaman => ORC_SHAMAN,
            Class::Warlock => ORC_WARLOCK,
        },
        Race::Dwarf => match class {
            Class::Shaman | Class::Mage | Class::Warlock | Class::Druid | Class::Warrior => {
                DWARF_WARRIOR
            }
            Class::Paladin => DWARF_PALADIN,
            Class::Hunter => DWARF_HUNTER,
            Class::Rogue => DWARF_ROGUE,
            Class::Priest => DWARF_PRIEST,
        },
        Race::NightElf => match class {
            Class::Paladin | Class::Shaman | Class::Mage | Class::Warlock | Class::Warrior => {
                NIGHT_ELF_WARRIOR
            }
            Class::Hunter => NIGHT_ELF_HUNTER,
            Class::Rogue => NIGHT_ELF_ROGUE,
            Class::Priest => NIGHT_ELF_PRIEST,
            Class::Druid => NIGHT_ELF_DRUID,
        },
        Race::Undead => match class {
            Class::Paladin | Class::Hunter | Class::Shaman | Class::Druid | Class::Warrior => {
                UNDEAD_WARRIOR
            }
            Class::Rogue => UNDEAD_ROGUE,
            Class::Priest => UNDEAD_PRIEST,
            Class::Mage => UNDEAD_MAGE,
            Class::Warlock => UNDEAD_WARLOCK,
        },
        Race::Tauren => match class {
            Class::Paladin
            | Class::Priest
            | Class::Mage
            | Class::Rogue
            | Class::Warlock
            | Class::Warrior => TAUREN_WARRIOR,
            Class::Hunter => TAUREN_HUNTER,
            Class::Shaman => TAUREN_SHAMAN,
            Class::Druid => TAUREN_DRUID,
        },
        Race::Gnome => match class {
            Class::Paladin
            | Class::Priest
            | Class::Shaman
            | Class::Druid
            | Class::Hunter
            | Class::Warrior => GNOME_WARRIOR,
            Class::Rogue => GNOME_ROGUE,
            Class::Mage => GNOME_MAGE,
            Class::Warlock => GNOME_WARLOCK,
        },
        Race::Troll => match class {
            Class::Paladin | Class::Warlock | Class::Druid | Class::Warrior => TROLL_WARRIOR,
            Class::Priest => TROLL_PRIEST,
            Class::Hunter => TROLL_HUNTER,
            Class::Rogue => TROLL_ROGUE,
            Class::Shaman => TROLL_SHAMAN,
            Class::Mage => TROLL_MAGE,
        },
        Race::Goblin => HUMAN_WARRIOR,
    };

    table[level]
}

#[derive(Debug, Copy, Clone)]
pub struct BaseStats {
    pub strength: u8,
    pub agility: u8,
    pub stamina: u8,
    pub intellect: u8,
    pub spirit: u8,
    pub health: u16,
    pub mana: u16,
}

impl BaseStats {
    pub const fn new(
        strength: u8,
        agility: u8,
        stamina: u8,
        intellect: u8,
        spirit: u8,
        health: u16,
        mana: u16,
    ) -> Self {
        Self {
            strength,
            agility,
            stamina,
            intellect,
            spirit,
            health,
            mana,
        }
    }
}

